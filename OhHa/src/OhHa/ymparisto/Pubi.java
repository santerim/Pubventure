package OhHa.ymparisto;

public class Pubi {

    private int leveys;
    private int korkeus;

    public Pubi(int leveys, int korkeus) {
        this.leveys = leveys;
        this.korkeus = korkeus;
    }

    public int getLeveys() {
        return this.leveys;
    }

    public int getKorkeus() {
        return this.korkeus;
    }
}
